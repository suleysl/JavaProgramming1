package day18_NestedLoop;

public class NestedLoopIntro {

    public static void main(String[] args) {


        for (int i = 0; i < 5; i++) {
            System.out.println("Wooden Spoon");
        }

        System.out.println("_________________________________________________");


        for (int j = 0; j < 4; j++) {
            for (int i = 0; i < 5; i++) {
                System.out.println("Wooden Spoon");

            }
        }








    }
}
