package day03_EscapeSequences;
/*
escape sequences: MUST be given with double quote
   \n: start a new line.
   \t: (tab) button on the keyboard, paragraph space
   \\: to make a single back slash
   \": To print double quote
 */

public class EscapeSequences {
    public static void main(String[] args) {

        System.out.println("Hello Cydeo! \nHow are you all today? \nIts nice to see you all! \nWhat class do we have next week?");
        System.out.println("\tJava is cool Programming Language");
        System.out.println("_____________________");
        System.out.println("/ \\");

        System.out.println("----------------------");

        System.out.println("my favorite tv show is \"Game of Thrones\""); //In order to print double quote we need to use bakclash and a quote





    }





}
